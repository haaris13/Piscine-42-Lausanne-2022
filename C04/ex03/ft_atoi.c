#include <unistd.h>

int	ft_atoi(const char *str)
{
	int	i;
	int	n;
	int	s;

	i = 0;
	n = 0;
	s = 1;
	while ((str[i] != '\0') && (str[i] == '\t') || (str[i] == '\n')
		|| (str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r')
		|| (str[i] == ' '))
		i++;
	while ((str[i] != '\0') && (str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			s = s * -1;
		i++;
	}
	while ((str[i] != '\0') && (str[i] >= '0') && (str[i] <= '9'))
	{
		n = n * 10;
		n = n + ((int)str[i] - 48);
		i++;
	}
	return (n * s);
}
